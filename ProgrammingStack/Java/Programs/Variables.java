package Programs;

public class Variables 
{
	public static void main(String args[])
	{
		int xyz;   // this statement when executed reserves space in memory and names this reserved space 'xyz'
		
		xyz = 100;  // this statement when executed assigns value 100 to the reserved space 'xyz'
		
		System.out.println(xyz);  // this statement when executed prints the value present in the reserved space 'xyz'
	}
}



